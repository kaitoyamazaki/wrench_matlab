% 修正されたm1データ
m1 = [0 0 0; 0 1 0.010; 0 2 0.020; 0 3 0.030; 0 4 0.040; 0 5 0.050; 0 6 0.060; 0 7 0.070; 0 8 0.080; 0 9 0.090; 0 10 0.100];

m1 = moment_f1
% 仮のm2データ（m1のz値を変更）
m2 = [0 0 0; 0 1 0.020; 0 2 0.040; 0 3 0.060; 0 4 0.080; 0 5 0.100; 0 6 0.120; 0 7 0.140; 0 8 0.160; 0 9 0.180; 0 10 0.200];
m2 = moment_f3
% それぞれの直線をプロット
plot3(m1(:,1), m1(:,2), m1(:,3), 'r');
hold on;
plot3(m2(:,1), m2(:,2), m2(:,3), 'b');

% 2つの直線間の領域をプロット
for i = 1:length(m1)
    for j = 1:length(m2)
        % m1とm2の点間の距離を計算
        d = norm(m1(i,:) - m2(j,:));
        if d <= 1 % 点間の距離が1未満の場合にプロット
            plot3([m1(i,1), m2(j,1)], [m1(i,2), m2(j,2)], [m1(i,3), m2(j,3)], 'k--');
        end
    end
end

% グラフの設定
xlabel('x');
ylabel('y');
zlabel('z');
title('3D Line Plot');
grid on;
axis equal;